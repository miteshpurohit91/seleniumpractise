public class lab020 {
    public static void main(String[] args)
    {
        //Relational Operator
        // >,<,>=, <=,=,
        int a = 10;
        int b = 10;
        boolean c = (a==b);
        System.out.println(c);
        System.out.println('A' == 65);

        // Logical Operators
        // !, &&,
        // ! : It can be apply to Boolean operator or a condition
        boolean f = true;
        System.out.println(f);
        System.out.println(!f);
        System.out.println(!(10>20));
    }
}
